package br.edu.psd.batalhanaval.Util;

public class SocketUtil {
  
	public static String converterNumeroEmLetra(int num) {
		String letra = "";
		if (num == 1)
			return "A";
		if (num == 2)
			return "B";
		if (num == 3) 
			return "C";
		if (num == 4) 
			return "D";
		if (num == 5)
			return "E";
		if (num == 6) 
			return "F";
		if (num == 7)
			return "G";
		if (num == 8) 
			return "H";
		if (num == 9) 
			return "I";
		if (num == 10) 
			return "J";
		if (num == 11)
			return "L";
		if (num == 12)
			return "M";
		if (num == 13) 
			return "N";
		if (num == 14) 
			return "O";
		if (num == 15)
			return "P";
		if (num == 1) 
			return "A";
		return letra;
	}
}
